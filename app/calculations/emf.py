"""
Неионизирующие излучения (ЭМП) — Приложение №17 к Приказу №33н.
"""

# Пороги кратности превышения ПДУ → kut по типам ЭМП
_EMF_RULES: dict[str, list[tuple[float, int]]] = {
    # (верхняя граница кратности включительно, kut)
    "esp":    [(1.0, 2), (5.0, 31), (float("inf"), 32)],    # электростатическое поле
    "pmp":    [(1.0, 2), (5.0, 31), (float("inf"), 32)],    # постоянное магн. поле
    "emp50":  [(1.0, 2), (5.0, 31), (10.0, 32), (40.0, 33), (float("inf"), 4)],  # ЭП 50 Гц
    "emp50m": [(1.0, 2), (5.0, 31), (10.0, 32), (float("inf"), 33)],             # МП 50 Гц
    "rf01":   [(1.0, 2), (5.0, 31), (10.0, 32), (float("inf"), 33)],  # 0.01–3 МГц
    "rf03":   [(1.0, 2), (3.0, 31), (5.0, 32), (10.0, 33), (float("inf"), 34)],  # 3–30 МГц
    "rf30":   [(1.0, 2), (3.0, 31), (5.0, 32), (10.0, 33), (100.0, 34), (float("inf"), 4)],  # 30–300 МГц
    "rf300":  [(1.0, 2), (3.0, 31), (5.0, 32), (10.0, 33), (100.0, 34), (float("inf"), 4)],  # 300 МГц–300 ГГц
    "ufi":    [(1.0, 2), (5.0, 31), (float("inf"), 32)],    # УФ-излучение
    "emp_rd": [(1.0, 2), (5.0, 31), (float("inf"), 32)],    # ЭМП рабочей документации (дефолт)
}


def classify_emf(emf_type: str, fact_value: float, pdu_value: float) -> int:
    """
    emf_type — тип поля (ключ из _EMF_RULES)
    fact_value — фактическое значение (в единицах ПДУ или В/м, А/м, мкТл...)
    pdu_value — нормативное значение ПДУ
    """
    if pdu_value <= 0:
        return 2
    ratio = fact_value / pdu_value
    rules = _EMF_RULES.get(emf_type, _EMF_RULES["emp_rd"])
    for upper, kut in rules:
        if ratio <= upper:
            return kut
    return rules[-1][1]
